CBBByteArray 
DIAMETER_avp::encode_binary()
{
	CBBByteArray	retval;
	unsigned int	flags_length;
	unsigned int 	type = getAvpTypeFromCode(_code, _vendor);
	
	if (_group_avp.size() > 0)
	{
		_value = CBBByteArray();

		for (unsigned int i = 0; i < _group_avp.size(); i++)
		{
			DIAMETER_avp gavp = _group_avp[i];
			CBBByteArray gavp_value = gavp.encode_binary();
			if (gavp_value.GetSize() > 0)
			{
				_value += gavp_value;
				if ((gavp_value.GetSize() % 4) > 0)
				{
					int padding = (4 - (gavp_value.GetSize() % 4));
					while (padding > 0)
					{
						_value += (CBBByte)0;
						padding--;
					}
				}
			}
		}
	}
	
	if (type == AVP_TYPE_NTP)
	{
		int offset = 0;
		if (_value.GetSize() > 0)
		{
			CBBString svalue = getValueAsString();
			offset = strtol(svalue.data(),(char **)0,10);
		}
		_value = getTimeValue(offset);
	}
	
	// Filter default values
	if ((_value.GetSize() == 0) && (_code != AVP_NAME_REQUESTED_SERVICE_UNIT))
	{
		return retval;
	}
	
	switch (_code)
	{
		case AVP_NAME_REDIRECT_SERVER_ADDRESS :
		case AVP_NAME_TGPP_GGSN_ADDRESS :
		case AVP_NAME_TGPP_PDP_ADDRESS :
		case AVP_NAME_TGPP_SGSN_ADDRESS :
		case AVP_NAME_TGPP_SMSC_ADDRESS :
		case AVP_NAME_TGPP_SERVED_PARTY_IP_ADDRESS :
		case AVP_NAME_TGPP_NEIGHBOR_NODE_ADDRESS :
		case AVP_NAME_HOST_IP_ADDRESS :
		{
			CBBString ip_address_s = getValueAsString();
			bool ipv6 = false;
			std::string::size_type idx = ip_address_s.find(":");
			if (idx != std::string::npos)
			{
				_value = CBBByteArray ();
				_value += (unsigned char)0;
				_value += (unsigned char)2;
				_value += colon_i(ip_address_s);
			}
			else
			{
				unsigned int ip_address_l = dd_i(ip_address_s);
				_value = CBBByteArray ();
				_value += (unsigned char)0;
				_value += (unsigned char)1;
				_value += (unsigned char)((ip_address_l >> 24) & 0xff);
				_value += (unsigned char)((ip_address_l >> 16) & 0xff);
				_value += (unsigned char)((ip_address_l >> 8) & 0xff);
				_value += (unsigned char)((ip_address_l >> 0) & 0xff);
			}
		}
		break;
		case AVP_NAME_TGPP_ORIGINATING_SCCP_ADDRESS :
		{
			unsigned long long signed_value;
			unsigned long long value = strtoull(getValueAsString().data(),(char **)0,16);
			//unsigned long long network_value = (long long)(htonll(signed_value));
			_value = CBBByteArray();
			_value += (unsigned char)0;
			_value += (unsigned char)8;
			int shift_value = 56;
			while ((((signed_value >> shift_value) & 0xff) == 0) &&
			       (shift_value >= 0))
			{
				shift_value -= 8;
			}
			while (shift_value >= 0)
			{
				_value += (unsigned char)((signed_value >> shift_value) & 0xff);
				shift_value -= 8;
			}
		}
		break;
		case AVP_NAME_CC_SERVICE_SPECIFIC_UNITS :
		case AVP_NAME_TARIFF_CHANGE_USAGE :
		case AVP_NAME_CURRENCY_CODE :
		case AVP_NAME_VALUE_DIGITS :
		case AVP_NAME_EXPONENT :
		case AVP_NAME_CC_INPUT_OCTETS :
		case AVP_NAME_CC_OUTPUT_OCTETS :
		case AVP_NAME_CC_TIME :
		//TEMP TEMP TEMP case AVP_NAME_CC_TOTAL_OCTETS : 
		case AVP_NAME_VALIDITY_TIME :
		case AVP_NAME_REDIRECT_MAX_CACHE_TIME :
		case AVP_NAME_SERVICE_IDENTIFIER :
		{
			// workaround for AVPs that have non-zero size but zero values
			bool all_zero = true;
			for (int j = 0; j < _value.GetSize(); j++)
			{
				if (_value[j] != (unsigned char)0) all_zero = false;
			}
			if (all_zero == true)
			{
				return retval;
			}
		}
		break;
		default :
		break;
	}
	
	// Filter other unused AVPs based on default values
	switch (type)
	{
		case AVP_TYPE_INT_32 :
		case AVP_TYPE_UINT_32 :
		{
			unsigned int temp_value = getValueAsInt();
			if (temp_value == AVP_TYPE_UNUSED_INT32)
			{
				return retval;
			}
		}
		case AVP_TYPE_INT_64 :
		case AVP_TYPE_UINT_64 :
		{
			unsigned long long temp_value = getValueAsLongLong();
			if (temp_value == AVP_TYPE_UNUSED_INT64)
			{
				return retval;
			}
		}
		default :
		{
		}
		break;
	}
	
	flags_length = _value.GetSize();
	bool mandatory = getAvpMandatoryFromCode(_code,_vendor);

	if (_vendor != 0)
	{
		diameter_vendor_avp_hdr_t hdr;
		flags_length += sizeof(diameter_vendor_avp_hdr_t);
		flags_length |= _avp_flags_vendor;
		
		if (mandatory == true)
		{
			flags_length |= _avp_flags_mandatory;
		}
		hdr._code = htonl(_code);
		hdr._flags_length = htonl(flags_length);
		hdr._vendor = htonl(_vendor);
		retval += CBBByteArray ((char *)&hdr,sizeof(diameter_vendor_avp_hdr_t));
	}
	else
	{
		diameter_avp_hdr_t hdr;
		flags_length += sizeof(diameter_avp_hdr_t);
		if (mandatory == true)
		{
			flags_length |= _avp_flags_mandatory;
		}
		hdr._code = htonl(_code);
		hdr._flags_length = htonl(flags_length);
		retval += CBBByteArray ((char *)&hdr,sizeof(diameter_avp_hdr_t));
	}
	
	retval += _value;
	return retval;
}
int
DIAMETER_avp::decode_binary (CBBByteArray &packet)
{
	int retval = 0;
	diameter_avp_hdr_t	hdr;

	if (packet.GetSize() < sizeof(diameter_avp_hdr_t)) return retval;
	
	memcpy ((char *)&hdr,packet.GetData(),sizeof(diameter_avp_hdr_t));
	unsigned int length = 0;
	unsigned int flags_length = ntohl(hdr._flags_length);
	if ((flags_length & _avp_flags_vendor) != 0)
	{
		diameter_vendor_avp_hdr_t	v_hdr;

		if (packet.GetSize() < sizeof(diameter_vendor_avp_hdr_t)) return retval;
		
		memcpy ((char *)&v_hdr,packet.GetData(),sizeof(diameter_vendor_avp_hdr_t));
		CBBByteArray jnk = packet.Extract(sizeof(diameter_vendor_avp_hdr_t));
		flags_length = ntohl(v_hdr._flags_length);
		_flags = (flags_length >> 24) & 0xff;
		_code = ntohl(v_hdr._code);
		_vendor = ntohl(v_hdr._vendor);
		length = flags_length & 0xffffff;
		retval = length;
		length -= sizeof(diameter_vendor_avp_hdr_t);
	}
	else
	{
		CBBByteArray jnk = packet.Extract(sizeof(diameter_avp_hdr_t));
		_code = ntohl(hdr._code);
		_flags = (flags_length >> 24) & 0xff;
		length = flags_length & 0xffffff;
		retval = length;
		length -= sizeof(diameter_avp_hdr_t);
	}
	if (length > (unsigned int)(packet.GetSize()))
	{
		retval = 0;
		return retval;
	}
	unsigned int type = getAvpTypeFromCode(_code, _vendor);
	_value = packet.Extract(length);
	if (type == AVP_TYPE_GROUPED)
	{
		bool recursion = true;

		while(recursion == true)
		{
			DIAMETER_avp	group_avp;
			int reduce = group_avp.decode_binary(_value);
			if (reduce > 0)
			{
				setAvp(group_avp);
				CBBByteArray recurse_jnk = _value.Extract(reduce);
			}
			else
			{
				recursion = false;
			}
		}
	}
	return retval;
}

CBBString DIAMETER_avp::encode_string()
{
	const CBBString start_tag("<avp>");
	const CBBString stop_tag("</avp>");
	const CBBString name_start_tag("<name>");
	const CBBString name_stop_tag("</name>");
	const CBBString value_start_tag("<value>");
	const CBBString value_stop_tag("</value>");
	CBBString name = getAvpNameFromCode(_code, _vendor);
	CBBString	retval;
	
	if (name.length() > 0)
	{
		retval += start_tag;
		retval += "\n";
		retval += name_start_tag;
		retval += name;
		retval += name_stop_tag;
		retval += "\n";
		if (_group_avp.size() > 0)
		{
			for (unsigned int i = 0; i < _group_avp.size(); i++)
			{
				retval += _group_avp[i].encode_string();
			}
		}
		else
		{
			retval += value_start_tag;
			unsigned int type = getAvpTypeFromCode(_code,_vendor);
			switch (type)
			{
				case AVP_TYPE_BYTES :
				{
					retval += _value.AsHexString();
				}
				break;
				case AVP_TYPE_STRING :
				{
					CBBString string_value((char *)_value.GetData(),_value.GetSize());
					retval += string_value;
				}
				break;
				case AVP_TYPE_INT_64 :
				case AVP_TYPE_INT_32 :
				{
					char cmd[256];
					long long signed_value = 0LL;
					for (int k = 0; k < _value.GetSize(); k++)
					{
						long long tmp_value = (long long)(_value[k]);
						signed_value <<= 8;
						signed_value |= tmp_value;
					}
					sprintf (cmd,"%lld",signed_value);
					retval += CBBString(cmd);
				}
				break;
				case AVP_TYPE_UINT_64 :
				case AVP_TYPE_UINT_32 :
				{
					char cmd[256];
					unsigned long long signed_value = 0LL;
					for (int k = 0; k < _value.GetSize(); k++)
					{
						unsigned long long tmp_value = (unsigned long long)(_value[k]);
						signed_value <<= 8;
						signed_value |= tmp_value;
					}
					sprintf (cmd,"0x%llx",signed_value);
					retval += CBBString(cmd);
				}
				break;
				default :
				case AVP_TYPE_NTP :
				case AVP_TYPE_FLOAT_32 :
				case AVP_TYPE_GROUPED :
				{
				}
				break;
			}
			retval += value_stop_tag;
			retval += "\n";
		}
		retval += stop_tag;
		retval += "\n";
	}
	return retval;
}

int DIAMETER_avp::decode_string(char *osp, int offset, int length)
{
	const CBBString comment_start_tag("<!--");
	const CBBString comment_stop_tag("-->");
	const CBBString start_tag("<avp>");
	const CBBString stop_tag("</avp>");
	const CBBString name_start_tag("<name>");
	const CBBString name_stop_tag("</name>");
	const CBBString value_start_tag("<value>");
	const CBBString value_stop_tag("</value>");
	const CBBString hex_base("0x");

	char *sp = osp;
	int retval = 0;

	// <avp>
	// <name>name</name>
	// <value>value</value>
	// </avp>  
	//
	// or
	// <avp>
	// <name>name</name>
	// <avp>

	char *name_start = (char *)0;
	char *name_end = (char *)0;
	char *value_start = (char *)0;
	char *value_end = (char *)0;
	bool in_comment_tag = false;
	bool in_name_tag = false;
	bool in_value_tag = false;
	bool in_avp_tag = false;
	bool in_stop_tag = false;
	// keep going as long as we can parse the </avp> end tag
	while (((offset + retval + (int)(stop_tag.length())) < length) &&
	       (in_stop_tag == false))
	{
 		if (strncmp(sp,start_tag.data(),start_tag.length()) == 0)
		{
			// if this is our not our start tag, it is a group start
			if (in_avp_tag == true)
			{
				DIAMETER_avp gavp;
				int gavp_retval = gavp.decode_string(sp,offset+retval,length);
				if (gavp_retval != 0)
				{
					_group_avp.push_back(gavp);
					retval += gavp_retval;
					sp += gavp_retval;
				}
				else
				{
					printf ("Error on '%s'\n",sp);
					return 0;
				}
			}
			else
			{
				in_avp_tag = true;
				sp += start_tag.length();
				retval += start_tag.length();
			}
		}
		else if (strncmp (sp,stop_tag.data(),stop_tag.length()) == 0)
		{
			sp += stop_tag.length();
			retval += stop_tag.length();
			in_stop_tag = true;
		}
		else if (strncmp (sp,name_start_tag.data(),name_start_tag.length()) == 0)
		{
			sp += name_start_tag.length();
			retval += name_start_tag.length();
			name_start = sp;
			name_end = sp;
			in_name_tag = true;
		}
		else if (strncmp (sp,name_stop_tag.data(),name_stop_tag.length()) == 0)
		{
			sp += name_stop_tag.length();
			retval += name_stop_tag.length();
			in_name_tag = false;
		}
		else if (strncmp (sp,comment_start_tag.data(),comment_start_tag.length()) == 0)
		{
			sp += comment_start_tag.length();
			retval += comment_start_tag.length();
			in_comment_tag = true;
		}
		else if (strncmp (sp,comment_stop_tag.data(),comment_stop_tag.length()) == 0)
		{
			sp += comment_stop_tag.length();
			retval += comment_stop_tag.length();
			in_comment_tag = false;
		}
		else if (strncmp (sp,value_start_tag.data(),value_start_tag.length()) == 0)
		{
			sp += value_start_tag.length();
			retval += value_start_tag.length();
			value_start = sp;
			value_end = sp;
			in_value_tag = true;
		}
		else if (strncmp (sp,value_stop_tag.data(),value_stop_tag.length()) == 0)
		{
			sp += value_stop_tag.length();
			retval += value_stop_tag.length();
			in_value_tag = false;
		}
		else if (in_name_tag == true)
		{
			name_end++;
			sp++;
			retval++;
		}
		else if (in_value_tag == true)
		{
			value_end++;
			sp++;
			retval++;
		}
		else
		{
			sp++;
			retval++;
		}
 	}
      	
	if ((name_start != (char *)0) && (name_end != (char *)0) && (name_start != name_end))
	{
		while (name_start != name_end)
		{
			if ((*name_start != ' ') &&
			    (*name_start != '\t') &&
			    (*name_start != '\r') &&
			    (*name_start != '\n'))
			{
				break;
			}
			name_start++;
		}
		while (name_end != name_start)
		{
			if ((*name_end != ' ') &&
			    (*name_end != '\t') &&
			    (*name_end != '\r') &&
			    (*name_end != '<') &&
			    (*name_end != '\n'))
			{
				name_end++;
				break;
			}
			name_end--;
		}
		if (name_start == name_end) name_end++; // single character
      		CBBString nameString = CBBString(name_start,name_end - name_start);
      		unsigned int code = getAvpCodeFromName(nameString);
      		unsigned int vendor = getAvpVendorFromName(nameString);
		if (code == 0) 
		{
			printf ("getAvpVendorFromName returns 0 for '%s'\n",nameString.data());
			return 0;
		}
		_code = code;
		_vendor = vendor;
	}
	else
	{
		printf ("name_string error on %s\n",osp);
		return 0;
	}
      	
	unsigned int type = getAvpTypeFromCode(_code,_vendor);
	
	if ((value_start != (char *)0) && (value_end != (char *)0))
	{
		while (value_start != value_end)
		{
			if ((*value_start != ' ') &&
			    (*value_start != '\t') &&
			    (*value_start != '\r') &&
			    (*value_start != '\n'))
			{
				break;
			}
			value_start++;
		}
		while (value_end != value_start)
		{
			if ((*value_end != ' ') &&
			    (*value_end != '\t') &&
			    (*value_end != '\r') &&
			    (*value_end != '<') &&
			    (*value_end != '\n'))
			{
				value_end++;
				break;
			}
			value_end--;
		}
		if ((value_start == value_end) && (type != AVP_TYPE_STRING))value_end++; // single character
      		CBBString valueString = CBBString(value_start,value_end - value_start);

		switch (type)
		{
			case AVP_TYPE_BYTES :
			{
				_value = CBBByteArray(valueString.data());
			}
			break;
			case AVP_TYPE_NTP :
			case AVP_TYPE_STRING :
			{
				_value = CBBByteArray(valueString.data(),valueString.length());
			}
			break;
			case AVP_TYPE_INT_32 :
			{
				int signed_value;
				if (strncmp(valueString.data(),hex_base.data(),hex_base.length()) == 0)
				{
					signed_value = strtol(valueString.data(),(char **)0,16);
				}
				else
				{
					signed_value = strtol(valueString.data(),(char **)0,10);
				}
				int network_value = htonl(signed_value);
				_value = CBBByteArray((char *)&network_value,sizeof(int));
			}
			break;
			case AVP_TYPE_UINT_32 :
			{
				unsigned int signed_value;
				if (strncmp(valueString.data(),hex_base.data(),hex_base.length()) == 0)
				{
					signed_value = strtoul(valueString.data(),(char **)0,16);
				}
				else
				{
					signed_value = strtoul(valueString.data(),(char **)0,10);
				}
				unsigned int network_value = htonl(signed_value);
				_value = CBBByteArray((char *)&network_value,sizeof(unsigned int));
			}
			break;
			case AVP_TYPE_INT_64 :
			{
				long long signed_value;
				if (strncmp(valueString.data(),hex_base.data(),hex_base.length()) == 0)
				{
					//signed_value = _strtoi64(valueString.data(),(char **)0,16);
					unsigned long long value = strtoull(valueString.data(),(char **)0,16);

				}
				else
				{
					// signed_value = _strtoi64(valueString.data(),(char **)0,10);
					unsigned long long value = strtoull(valueString.data(),(char **)0,10);
				}
				long long network_value = (long long)(htonll(signed_value));
				_value = CBBByteArray((char *)&network_value,sizeof(long long));
			}
			break;
			case AVP_TYPE_UINT_64 :
			{
				unsigned long long signed_value;
				if (strncmp(valueString.data(),hex_base.data(),hex_base.length()) == 0)
				{
					//signed_value = _strtoui64(valueString.data(),(char **)0,16);
					unsigned long long value = strtoull(valueString.data(),(char **)0,16);
				}
				else
				{
					//signed_value = _strtoui64(valueString.data(),(char **)0,10);
					unsigned long long value = strtoull(valueString.data(),(char **)0,10);
				}
				unsigned long long network_value = (long long)(htonll(signed_value));
				_value = CBBByteArray((char *)&network_value,sizeof(unsigned long long));
			}
			break;
			default :
			case AVP_TYPE_FLOAT_32 :
			case AVP_TYPE_GROUPED :
			{
				printf ("Unknown type for %s\n",osp);
				return 0;
			}
			break;
		}
		return retval;
	}
	else if (type == AVP_TYPE_GROUPED)
	{
		return retval;
	}
	else
	{
		printf ("Unknown type for %s\n",osp);
		return 0;
	}
}

