	CBBByteArray	retval;
	unsigned int now_time = (unsigned int)(time((time_t *)0));
	if (offset >= 0) now_time += offset;
	else
	{
		unsigned int abs_time = abs(offset);
		now_time -= abs_time;
	}
	now_time += ntp_basetime;
	unsigned int network_time = htonl(now_time);
	retval = CBBByteArray ((char *)&network_time,sizeof(network_time));
	return retval;
