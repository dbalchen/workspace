table a check is made against the
<b>L9_(</b> <i>First/Second</i> <b>)_Threshold</b> fields. If an overage
is detected the above fields, The <b>CTN<sup><a id="fnr.4" class="footref" href="#fn.4">4</a></sup></b> is added to a file 
in the <b>NTF (See below)</b> directory. <b>MFT<sup><a id="fnr.5" class="footref" href="#fn.5">5</a></sup></b> then pulls these
files and delivers to <b>DMI<sup><a id="fnr.6" class="footref" href="#fn.6">6</a></sup></b> for distribution. A note is added to the
<b>Notification_Hub.Sms_Notification</b> table (<b>ODS</b>) indicating the
message was sent by <b>DMI</b>.
</p>
