

select CIBER_FILE_NAME_1||CIBER_FILE_NAME_2,count(*), sum(usage),sum(TOTAL_CHRG_AMOUNT)  from USC_ROAM_EVNTS where generated_rec < 2 and (REGEXP_LIKE (CIBER_FILE_NAME_1, 'CIBER_CIBER_2016121[6,7,8,9](*)' )  
or CIBER_FILE_NAME_1 like 'CIBER_CIBER_2016123%' or CIBER_FILE_NAME_1 like 'CIBER_CIBER_2016122%' or CIBER_FILE_NAME_1 like 'CIBER_CIBER_2017010%'
 or REGEXP_LIKE (CIBER_FILE_NAME_1, 'CIBER_CIBER_2017011[0,1,2,3,4,5](*)' )  )
group by  CIBER_FILE_NAME_1||CIBER_FILE_NAME_2  order by CIBER_FILE_NAME_1||CIBER_FILE_NAME_2;

